#Slicing: taking a subset from set of data
# 2:7 
# 2:9:2 third number is the step
#creating a list
letters = ['A','B','C','D','E','F','G','H','I']
print(letters)
#%%
letters[:]
print(letters[2:])

#%%
letters = ['A','B','C','D','E','F','G','H','I','J']
print(letters[2:])

#%%
print(letters[:7])

#%%
print(letters[2:7])

#%%
print(letters[-8:7])
#%%
#Advanced slicing
print(letters[2:9])
#%%
#Advanced slicing
print(letters[2:9:2])
#%%
#Advanced slicing
print(letters[2:9])
#%%
#Advanced slicing
print(letters[2:9:3])
#%%
#Advanced slicing
print(letters[::3])
#%%
one = [1,2,3,4,5,6,7,8,9]
print(one)
#%%
print(one[:])
#%%
print(one[::3])


























